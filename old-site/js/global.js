
function toggleLayer(divID){
	var e;
	e = document.getElementById(divID);
	e.style.visibility = (e.style.visibility == 'visible' ? 'hidden' : 'visible');
	e.style.display = (e.style.display == 'block' ? 'none' : 'block');	
	}
	
